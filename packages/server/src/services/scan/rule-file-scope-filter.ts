export type FileScopeCategory =
	| "runtime-code"
	| "test"
	| "fixture"
	| "documentation"
	| "example"
	| "configuration"
	| "generated"
	| "vendor"
	| "asset"
	| "unknown";

export type FileScopeAction = "include" | "exclude";

export type FileScopeClassification = {
	path: string;
	category: FileScopeCategory;
	action: FileScopeAction;
	reason: string;
};

export type ModuleScopeClassification = {
	category: FileScopeCategory;
	action: FileScopeAction;
	reason: string;
};

